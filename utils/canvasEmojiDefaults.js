const DEFAULT_CANVAS_FALLBACK_EMOJI = '<:Inforect:1473038624172937287>';

function normalizeEmojiHint(value = '') {
    return String(value || '')
        .trim()
        .replace(/<a?:([\w-]+):(\d+)>/gi, '$1')
        .replace(/[\uFE0E\uFE0F]/g, '')
        .replace(/[\u200D]/g, '')
        .replace(/[:_\-]+/g, ' ')
        .toLowerCase()
        .trim();
}

function parseCustomEmojiTag(tag = '') {
    const match = String(tag).match(/^<(a?):([^:]+):(\d+)>$/);
    if (!match) return null;
    return {
        animated: match[1] === 'a',
        name: match[2],
        id: match[3],
        tag: match[0]
    };
}

function resolveDefaultCanvasCustomEmoji(input = '') {
    const raw = String(input || '').trim();
    if (!raw) return null;

    if (DEFAULT_CANVAS_CUSTOM_EMOJIS[raw]) {
        return DEFAULT_CANVAS_CUSTOM_EMOJIS[raw];
    }

    const normalized = normalizeEmojiHint(raw);
    if (DEFAULT_CANVAS_CUSTOM_EMOJIS[normalized]) {
        return DEFAULT_CANVAS_CUSTOM_EMOJIS[normalized];
    }

    const tokens = normalized.split(/\s+/).filter(Boolean);
    for (const token of tokens) {
        if (DEFAULT_CANVAS_CUSTOM_EMOJIS[token]) {
            return DEFAULT_CANVAS_CUSTOM_EMOJIS[token];
        }
    }

    return DEFAULT_CANVAS_FALLBACK_EMOJI;
}

function getCanvasEmojiAssetUrl(input = '') {
    const resolved = resolveDefaultCanvasCustomEmoji(input);
    const parsed = resolved ? parseCustomEmojiTag(resolved) : null;
    if (!parsed) return null;
    return `https://cdn.discordapp.com/emojis/${parsed.id}.${parsed.animated ? 'gif' : 'png'}?size=128&quality=lossless`;
}

