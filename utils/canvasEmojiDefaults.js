const DEFAULT_CANVAS_FALLBACK_EMOJI = '<:Inforect:1473038624172937287>';

/* ────────────────── Twemoji codepoint → URL ────────────────── */

// Twemoji is published as one PNG per codepoint sequence, e.g.
// 1f600.png for 😀, 1f3c6.png for 🏆, 1f468-200d-1f4bb.png for ZWJ
// sequences like 👨‍💻. Discord uses the same set, so any unicode emoji
// can be rendered as an image as long as we encode the codepoints
// correctly. We use jsDelivr's mirror of jdecked/twemoji (the actively
// maintained fork after Twitter archived the original repo) pinned to
// a tagged release so the URLs are stable and CDN-cached.
const TWEMOJI_BASE = 'https://cdn.jsdelivr.net/gh/jdecked/twemoji@15.1.0/assets/72x72';

/**
 * Convert a Unicode emoji string into Twemoji's codepoint filename.
 *  - Surrogate pairs are combined into a single codepoint.
 *  - U+FE0F (variation selector-16) is stripped UNLESS the emoji is a
 *    keycap sequence (where FE0F is required by Twemoji's filename).
 *  - U+200D (ZWJ) is preserved so multi-person/family sequences work.
 */
function emojiToTwemojiCodepoint(emoji) {
    if (!emoji) return null;
    const codepoints = [];
    let high = 0;
    for (let i = 0; i < emoji.length; i++) {
        const c = emoji.charCodeAt(i);
        if (high) {
            codepoints.push(0x10000 + ((high - 0xD800) << 10) + (c - 0xDC00));
            high = 0;
        } else if (c >= 0xD800 && c <= 0xDBFF) {
            high = c;
        } else {
            codepoints.push(c);
        }
    }

    // Keycap sequences (e.g. 1️⃣ = 0031 FE0F 20E3) need FE0F kept.
    const isKeycap = codepoints.includes(0x20E3);
    // ZWJ sequences need FE0F stripped on jdecked/twemoji.
    const stripVariation = !isKeycap;
    const filtered = stripVariation
        ? codepoints.filter(cp => cp !== 0xFE0F)
        : codepoints;

    if (!filtered.length) return null;
    return filtered.map(cp => cp.toString(16)).join('-');
}

/**
 * Detect whether a string is (or starts with) a Unicode emoji rather
 * than a plain word like "money" or a custom-emoji tag.
 */
function isUnicodeEmojiString(str = '') {
    return /[\u{1F000}-\u{1FFFF}\u{2600}-\u{27BF}\u{2300}-\u{23FF}\u{2B00}-\u{2BFF}\u{1F1E6}-\u{1F1FF}\u{20E3}]/u.test(str);
}

function normalizeEmojiHint(value = '') {
    return String(value || '')
        .trim()
        .replace(/<a?:([\w-]+):(\d+)>/gi, '$1')
        .replace(/[\uFE0E\uFE0F]/g, '')
        .replace(/[\u200D]/g, '')
        .replace(/[:_\-]+/g, ' ')
        .toLowerCase()
        .trim();
}

function parseCustomEmojiTag(tag = '') {
    const match = String(tag).match(/^<(a?):([^:]+):(\d+)>$/);
    if (!match) return null;
    return {
        animated: match[1] === 'a',
        name: match[2],
        id: match[3],
        tag: match[0]
    };
}

function resolveDefaultCanvasCustomEmoji(input = '') {
    const raw = String(input || '').trim();
    if (!raw) return null;

    // Discord custom emoji tag — already fully qualified, no need to remap.
    // Without this short-circuit, "<:Volumeup:1473…>" gets normalised to
    // "volumeup", misses the override table, and falls through to the
    // generic Inforect fallback. That's why every stat-box icon used to
    // render as the same info badge.
    if (parseCustomEmojiTag(raw)) {
        return raw;
    }

    // Direct hit on the explicit overrides table (e.g. 💰 → Money custom emoji)
    if (DEFAULT_CANVAS_CUSTOM_EMOJIS[raw]) {
        return DEFAULT_CANVAS_CUSTOM_EMOJIS[raw];
    }

    const normalized = normalizeEmojiHint(raw);
    if (DEFAULT_CANVAS_CUSTOM_EMOJIS[normalized]) {
        return DEFAULT_CANVAS_CUSTOM_EMOJIS[normalized];
    }

    const tokens = normalized.split(/\s+/).filter(Boolean);
    for (const token of tokens) {
        if (DEFAULT_CANVAS_CUSTOM_EMOJIS[token]) {
            return DEFAULT_CANVAS_CUSTOM_EMOJIS[token];
        }
    }

    // Anything that's a real Unicode emoji but not in the override table
    // should be returned as-is so the caller can route it through Twemoji
    // (preserves 🐺, 🍒, 🥇, 😀, …). Returning the fallback custom emoji
    // here would smear every animal/food/face into the same generic icon.
    if (isUnicodeEmojiString(raw)) return raw;

    return DEFAULT_CANVAS_FALLBACK_EMOJI;
}

function getCanvasEmojiAssetUrl(input = '') {
    const resolved = resolveDefaultCanvasCustomEmoji(input);
    if (!resolved) return null;

    // Custom Discord emoji tag: serve from Discord CDN.
    const parsed = parseCustomEmojiTag(resolved);
    if (parsed) {
        return `https://cdn.discordapp.com/emojis/${parsed.id}.${parsed.animated ? 'gif' : 'png'}?size=128&quality=lossless`;
    }

    // Plain Unicode emoji: serve from Twemoji.
    if (isUnicodeEmojiString(resolved)) {
        const cp = emojiToTwemojiCodepoint(resolved);
        if (cp) return `${TWEMOJI_BASE}/${cp}.png`;
    }

    return null;
}

